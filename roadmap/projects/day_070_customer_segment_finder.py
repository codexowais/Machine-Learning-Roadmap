# Day 070 Mini Project: Customer Segment Finder
#
# Real-world use case:
# Segment customers into behavior groups so a business can understand budget
# shoppers, loyal buyers, premium customers, and unusual accounts.
#
# Skills from days 061-070:
# clustering, K-Means, elbow method, silhouette score, PCA, anomaly detection,
